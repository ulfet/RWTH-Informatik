package Q4;

public class Student
{
    private String uni = ""; // initially unknown
    private int id = 0; // initially no id

    public int getIdentifier()
    {
        return id;
    }

    public void setIdentifier(int i)
    {
        this.id = i;
    }

    public String getUni()
    {
        return uni;
    }

    public void setUni(String u)
    {
        this.uni = u;
    }

}
